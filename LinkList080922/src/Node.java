
public class Node {
	private int data;
	private int ref;
	public Node(int data, int ref) {
		super();
		this.data = data;
		this.ref = ref;
	}
	public Node() {}
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public int getRef() {
		return ref;
	}
	public void setRef(int ref) {
		this.ref = ref;
	}
	
	
	
	
}
